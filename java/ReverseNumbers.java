public class ReverseNumbers {
static void reverseNumber(){
   System.out.println("Displaying numbers from 10 to 0.");
    for(int i=10; i>=0; i--){
         System.out.println(i);
    }
}
    public static void main(String[] args) {
        reverseNumber();
    }
}
